import { onMounted } from 'vue'
import mapboxgl from 'mapbox-gl'


onMounted(() => {
  const dateContainer = document.getElementById('dateDropdownContainer');
  const calendarDropdown = document.getElementById('calendarDropdown');

  if (dateContainer && calendarDropdown) {
    dateContainer.addEventListener('click', (event) => {
      event.stopPropagation();

      const isVisible = calendarDropdown.style.display === 'block';
      calendarDropdown.style.display = isVisible ? 'none' : 'block';
    });

    document.addEventListener('click', (event) => {
      if (!event.target.closest('#dateDropdownContainer')) {
        calendarDropdown.style.display = 'none';
      }
    });
  }
});

</script>

